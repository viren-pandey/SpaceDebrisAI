// ── Component ────────────────────────────────────────────────────────────────
export default function ApiPage() {
  const [email, setEmail]       = useState("");
  const [apiKey, setApiKey]     = useState("");
  const [copied, setCopied]     = useState(false);
  const [expanded, setExpanded] = useState({});
  const [codeLang, setCodeLang] = useState("python");
  const [codeCopied, setCodeCopied] = useState(false);

  // Restore saved key
  useEffect(() => {
    const saved = localStorage.getItem("sdai_api_key");
    if (saved) setApiKey(saved);
  }, []);

  function handleGenerate(e) {
    e.preventDefault();
    if (!email.trim()) return;
    const key = generateApiKey(email.trim());
    setApiKey(key);
    localStorage.setItem("sdai_api_key", key);
  }

  function handleCopyKey() {
    navigator.clipboard.writeText(apiKey).then(() => {
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    });
  }

  function handleCopyCode() {
    navigator.clipboard.writeText(CODE[codeLang](apiKey || "YOUR_API_KEY")).then(() => {
      setCodeCopied(true);
      setTimeout(() => setCodeCopied(false), 2000);
    });
  }

  function toggleExpand(path) {
    setExpanded((prev) => ({ ...prev, [path]: !prev[path] }));
  }

  const displayKey = apiKey || "sdai_xxxxxxxxxxxxxxxx_live";

  return (
    <div className="ap-root">
      {/* ── Hero ── */}
      <div className="ap-hero">
        <div className="ap-hero-inner">
          <p className="ap-eyebrow">Developer Access</p>
          <h1 className="ap-h1">
            SpaceDebrisAI<br />
            <span className="ap-h1-accent">Public API</span>
          </h1>
          <p className="ap-sub">
            Real-time satellite positions, proximity risk classifications, and
            AI-generated avoidance maneuvers — all available via a simple REST API.
            Built on SGP4 propagation over{" "}
            <strong style={{ color: "#fff" }}>200 tracked objects</strong>.
          </p>
          <div className="ap-badges">
            <span className="ap-badge">REST</span>
            <span className="ap-badge">JSON</span>
            <span className="ap-badge">Free tier</span>
            <span className="ap-badge ap-badge-live">● Live data</span>
          </div>
        </div>
      </div>

      <div className="ap-body">
        {/* ── Get API Key ── */}
        <section className="ap-section">
          <h2 className="ap-section-title">Get your API key</h2>
          <div className="ap-key-card">
            {!apiKey ? (
              <>
                <p className="ap-key-intro">
                  Enter your email to generate a free API key. No password required —
                  the key is scoped to this browser and stored locally.
                </p>
                <form className="ap-key-form" onSubmit={handleGenerate}>
                  <input
                    type="email"
                    className="ap-input"
                    placeholder="you@example.com"
                    value={email}
                    onChange={(e) => setEmail(e.target.value)}
                    required
                  />
                  <button type="submit" className="ap-btn-primary">
                    Generate key
                  </button>
                </form>
              </>
            ) : (
              <>
                <p className="ap-key-intro ap-key-success">
                  ✓ Your API key is ready. Include it as the{" "}
                  <code className="ap-inline-code">X-API-Key</code> header in every
                  request.
                </p>
                <div className="ap-key-display">
                  <span className="ap-key-value">{apiKey}</span>
                  <button className="ap-btn-copy" onClick={handleCopyKey}>
                    {copied ? "Copied ✓" : "Copy"}
                  </button>
                </div>
                <button
                  className="ap-btn-text"
                  onClick={() => {
                    setApiKey("");
                    localStorage.removeItem("sdai_api_key");
                    setEmail("");
                  }}
                >
                  Revoke &amp; generate new key
                </button>
              </>
            )}
          </div>
        </section>

        {/* ── Base URL ── */}
        <section className="ap-section">
          <h2 className="ap-section-title">Base URL</h2>
          <div className="ap-base-url">
            <span className="ap-base-label">Production</span>
            <code className="ap-code-block">{BASE}</code>
          </div>
        </section>

        {/* ── Endpoints ── */}
        <section className="ap-section">
          <h2 className="ap-section-title">Endpoints</h2>
          <div className="ap-endpoints">
            {ENDPOINTS.map((ep) => (
              <div key={ep.path} className="ap-endpoint">
                <div
                  className="ap-endpoint-header"
                  onClick={() => toggleExpand(ep.path)}
                >
                  <span
                    className="ap-method"
                    style={{ color: METHOD_COLOR[ep.method] }}
                  >
                    {ep.method}
                  </span>
                  <span className="ap-path">{ep.path}</span>
                  <span className="ap-endpoint-desc-short">{ep.desc.split(".")[0]}.</span>
                  <span className="ap-expand-icon">
                    {expanded[ep.path] ? "−" : "+"}
                  </span>
                </div>

                {expanded[ep.path] && (
                  <div className="ap-endpoint-body">
                    <p className="ap-endpoint-full-desc">{ep.desc}</p>

                    {ep.isLink ? (
                      <a
                        href={`${BASE}${ep.path}`}
                        target="_blank"
                        rel="noopener noreferrer"
                        className="ap-btn-primary ap-docs-btn"
                      >
                        Open Swagger UI →
                      </a>
                    ) : (
                      <>
                        <p className="ap-label">Example response</p>
                        <pre className="ap-pre">{ep.example}</pre>
                      </>
                    )}
                  </div>
                )}
              </div>
            ))}
          </div>
        </section>

        {/* ── Code examples ── */}
        <section className="ap-section">
          <h2 className="ap-section-title">Code examples</h2>
          <div className="ap-code-card">
            <div className="ap-code-tabs">
              {["python", "javascript", "curl"].map((lang) => (
                <button
                  key={lang}
                  className={`ap-code-tab${codeLang === lang ? " active" : ""}`}
                  onClick={() => setCodeLang(lang)}
                >
                  {lang}
                </button>
              ))}
              <button className="ap-btn-copy ap-code-copy" onClick={handleCopyCode}>
                {codeCopied ? "Copied ✓" : "Copy"}
              </button>
            </div>
            <pre className="ap-pre ap-code-body">
              {CODE[codeLang](displayKey)}
            </pre>
          </div>
        </section>

        {/* ── Limits note ── */}
        <section className="ap-section">
          <div className="ap-limits-card">
            <h3 className="ap-limits-title">Rate limits &amp; fair use</h3>
            <div className="ap-limits-grid">
              <div className="ap-limit-item">
                <span className="ap-limit-val">60</span>
                <span className="ap-limit-label">requests / minute</span>
              </div>
              <div className="ap-limit-item">
                <span className="ap-limit-val">200</span>
                <span className="ap-limit-label">satellites tracked</span>
              </div>
              <div className="ap-limit-item">
                <span className="ap-limit-val">SGP4</span>
                <span className="ap-limit-label">propagation model</span>
              </div>
              <div className="ap-limit-item">
                <span className="ap-limit-val">Free</span>
                <span className="ap-limit-label">no billing</span>
              </div>
            </div>
            <p className="ap-limits-note">
              This is a research &amp; demonstration API. Data is refreshed on each
              request using the latest onboard TLE database. For production use at
              scale, contact the maintainer.
            </p>
          </div>
        </section>
      </div>
    </div>
  );
}
