// ── Deterministic-looking API key from email ─────────────────────────────────
function generateApiKey(email) {
  let h = 5381;
  for (let i = 0; i < email.length; i++) {
    h = (Math.imul(h, 31) + email.charCodeAt(i)) | 0;
  }
  const seg1 = Math.abs(h).toString(16).padStart(8, "0");
  const seg2 = Date.now().toString(16).slice(-8);
  const seg3 = Math.floor(Math.random() * 0xffff)
    .toString(16)
    .padStart(4, "0");
  return `sdai_${seg1}${seg2}${seg3}_live`;
}

